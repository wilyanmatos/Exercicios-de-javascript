// Operadores Bitwise
let n1 = 100
let n2 = 11

//and
let res1 = n1 & n2
console.log(res1) 

//or
let res2 = n1 | n2
console.log(res2) 

//sor
let res3 = n1 ^ n2
console.log(res3)

// << Descola zero para a esquerda
let res4 = n1 << 1
console.log(res4)

// >> Desloca o zero para a direita
let res5 = n1 >> 2
console.log(res5)